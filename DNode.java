
public class DNode {
	
	private String data;
	private DNode prev;
	private DNode next;
	
	public String getData() {
		return data;
	}
	public void setData(String data) {
		this.data = data;
	}
	public DNode getPrev() {
		return prev;
	}
	public void setPrev(DNode prev) {
		this.prev = prev;
	}
	public DNode getNext() {
		return next;
	}
	public void setNext(DNode next) {
		this.next = next;
	}
	
	

}
